
using System;

public class Operacoes
{
	public Operacoes()
	{
	}
	public double Soma(double a, double b)
		{
			return a + b;
		}
	public double Subtracao(double a, double b)
	{
		return a - b;
	}
	public double Multiplicacao(double a, double b)
	{
		return a * b;
	}
	public double Dvisao(double a, double b)
	{
		return a / b;
	}
	public double Porcetangem(double a, double b)
	{
		return a * (b / 100);
	}
}